#!/usr/bin/env python3
"""Generate data/parma_vertical_spectrum.csv for the source-spectrum figures.

Mode 3 (PARMA/EXPACS) is the one UCMuon source model with no closed form:
it is a numerical model that lives only as Fortran in src/parma/.  Every
other curve in plot_source_spectra.py comes from a pure-Python port in
ucmuon_spectra.py, so PARMA was missing from the figures.  This script
runs the *real* PARMA subroutines once and caches the vertical muon
spectrum as a CSV, so the plotting code can treat Mode 3 like any other
reference curve without needing a Fortran compiler.

It compiles two small Fortran helpers against the PARMA sources and runs
them at the sea-level reference site used by the manuscript figures
(mid-latitude, zero solar modulation):

  * parma_vertical_spec.f90  -> vertical muon spectrum I_V(T), converted to
    per-momentum:  I_V(p) = I_V(T)*dT/dp, dT/dp = p/E, E = T+m, p = sqrt(E^2-m^2)
  * parma_angular_spec.f90   -> angular factor F_ang(E, theta) at fixed
    momenta, for the zenith-dependence figure.

Outputs:
    data/parma_vertical_spectrum.csv  (columns: p_gevc, IV_per_gevc)
    data/parma_angular.csv            (columns: p0_gevc, theta_deg, F_ang)

Requires gfortran.  Run once; commit the CSVs.  Re-run with --rerun to
regenerate.

Usage:  python3 make_parma_spectrum.py [--rerun]
"""
import os
import subprocess
import sys

import numpy as np

HERE = os.path.dirname(os.path.abspath(__file__))
REPO = os.path.abspath(os.path.join(HERE, "..", ".."))
DATA = os.path.join(HERE, "data")
OUT_SPEC = os.path.join(DATA, "parma_vertical_spectrum.csv")
OUT_ANG = os.path.join(DATA, "parma_angular.csv")

PARMA_DATA = os.path.join(REPO, "data", "EXPACS", "parma")
SPEC_SRC = os.path.join(REPO, "manuscript", "scripts", "parma_vertical_spec.f90")
ANG_SRC = os.path.join(HERE, "parma_angular_spec.f90")
PARMA_F90 = [
    os.path.join(REPO, "src", "parma", "parma_path_module.f90"),
    os.path.join(REPO, "src", "parma", "parma_subroutines.f90"),
]

MUON_MASS = 0.10566  # GeV/c^2

# Reference site (identical to the manuscript fig02 PARMA curve):
# sea level, mid-latitude European site, zero solar modulation.
SITE = dict(lat=50.67, lon=4.62, alt_km=0.0, year=2026, month=1, day=1, W=0.0)
T_MIN, T_MAX, NPTS = 0.15, 3000.0, 200       # kinetic-energy grid [GeV]
ANG_P0 = (1.0, 10.0, 100.0)                  # momenta for the zenith figure
ANG_THETA_MAX, ANG_NTH = 85.0, 120           # zenith grid [deg]


def _site_args():
    return [SITE["lat"], SITE["lon"], SITE["alt_km"],
            SITE["year"], SITE["month"], SITE["day"], SITE["W"]]


def build_helper(src, exe):
    cmd = ["gfortran", "-O2", "-ffree-line-length-none", "-o", exe,
           *PARMA_F90, src, "-J", os.path.dirname(exe)]
    print("[build]", os.path.basename(src))
    r = subprocess.run(cmd, capture_output=True, text=True)
    if r.returncode != 0:
        sys.exit(f"PARMA helper build failed:\n{r.stderr}")


def run_helper(exe, tail):
    stdin = "\n".join(str(v) for v in [PARMA_DATA, *_site_args(), *tail]) + "\n"
    r = subprocess.run([exe], input=stdin, capture_output=True, text=True,
                       cwd=REPO)
    if r.returncode != 0:
        sys.exit(f"PARMA helper run failed:\n{r.stderr}")
    rows = [[float(v) for v in ln.split()]
            for ln in r.stdout.splitlines() if ln.strip()]
    return np.array(rows)


SITE_HDR = (f"# site: lat={SITE['lat']} lon={SITE['lon']} alt={SITE['alt_km']} "
            f"km ({SITE['year']}-{SITE['month']:02d}-{SITE['day']:02d}), "
            f"solar W={SITE['W']}\n")
PARMA_HDR = ("# PHITS-based Analytical Radiation Model in the Atmosphere, "
             "PARMA v4.10 (Sato 2015, 2016)\n")


def gen_spectrum():
    exe = os.path.join(HERE, "_parma_spec")
    build_helper(SPEC_SRC, exe)
    data = run_helper(exe, [T_MIN, T_MAX, NPTS])
    os.remove(exe)

    T = data[:, 0]            # kinetic energy [GeV]
    iv_T = data[:, 1]         # I_V per kinetic energy [cm^-2 s^-1 sr^-1 GeV^-1]
    E = T + MUON_MASS
    p = np.sqrt(E**2 - MUON_MASS**2)
    iv_p = iv_T * (p / E)     # per momentum [... (GeV/c)^-1]

    with open(OUT_SPEC, "w") as f:
        f.write("# PARMA/EXPACS vertical muon spectrum (Mode 3), generated by "
                "make_parma_spectrum.py\n" + PARMA_HDR + SITE_HDR +
                "# I_V = (mu+ + mu-) vertical intensity, converted to "
                "per-momentum via dT/dp = p/E\n"
                "# columns: p_gevc,IV_per_gevc\n")
        for pi, ii in zip(p, iv_p):
            f.write(f"{pi:.6e},{ii:.6e}\n")
    print(f"[ok] wrote {OUT_SPEC}  ({len(p)} points, "
          f"p = {p[0]:.3g}..{p[-1]:.3g} GeV/c)")


def gen_angular():
    exe = os.path.join(HERE, "_parma_ang")
    build_helper(ANG_SRC, exe)
    with open(OUT_ANG, "w") as f:
        f.write("# PARMA/EXPACS muon angular factor F_ang(E, theta) (Mode 3), "
                "generated by make_parma_spectrum.py\n" + PARMA_HDR + SITE_HDR +
                "# F_ang at fixed momenta p0; the zenith figure plots "
                "F_ang(theta)/F_ang(0)\n"
                "# columns: p0_gevc,theta_deg,F_ang\n")
        for p0 in ANG_P0:
            T0 = np.sqrt(p0**2 + MUON_MASS**2) - MUON_MASS  # kinetic energy
            data = run_helper(exe, [T0, ANG_THETA_MAX, ANG_NTH])
            for th, ang in data:
                f.write(f"{p0:.6e},{th:.5f},{ang:.6e}\n")
    os.remove(exe)
    print(f"[ok] wrote {OUT_ANG}  (p0 = {', '.join(f'{x:g}' for x in ANG_P0)} "
          f"GeV/c, {ANG_NTH} angles each)")


def main():
    if (os.path.exists(OUT_SPEC) and os.path.exists(OUT_ANG)
            and "--rerun" not in sys.argv):
        print("CSVs exist; pass --rerun to regenerate")
        return
    if not os.path.isdir(PARMA_DATA):
        sys.exit(f"PARMA data dir not found: {PARMA_DATA}")
    os.makedirs(DATA, exist_ok=True)
    gen_spectrum()
    gen_angular()
    # tidy the .mod left by gfortran -J
    for m in ("parma_path.mod",):
        if os.path.exists(os.path.join(HERE, m)):
            os.remove(os.path.join(HERE, m))


if __name__ == "__main__":
    main()
